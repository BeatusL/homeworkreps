#include <stdio.h>

int main() {
    int x; // Переменная для ввода значения x
    int y; // Результат функции
    int z; // Переменная, меняющаяся в цикле

    // Запрашиваем у пользователя ввод значения x
    printf("Введите значение x: ");
    scanf("%d", &x);

    // Цикл от 0 до 10 с шагом 1
    for (z = 0; z <= 10; z++) {
        // Вычисляем значение функции
        y = x * x + 3 * x - 5 + z;

        // Выводим результат в консоль
        printf("Значение функции при x=%d, z=%d: %d\n", x, z, y);
    }

    return 0;
}
